package app.adapters.out.MySQL;

import app.adapters.out.MySQL.entity.CustomerEntity;
import app.adapters.out.MySQL.repositories.CustomRepository;
import app.domain.models.Transaction;
import app.domain.port.CustomerDao;
import app.domain.models.Customer;
import jakarta.persistence.EntityNotFoundException;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.stereotype.Component;

import java.util.Optional;
import java.util.UUID;

@Component
public class CustomerDaoAdapter implements CustomerDao {
    private final CustomRepository customRepository;

    public CustomerDaoAdapter(CustomRepository customRepository) {
        this.customRepository = customRepository;
    }

    @Override
    public void addCustomer(Customer customer) {
        CustomerEntity customerEntity = CustomerEntity.builder()
                .name(customer.getName())
                .email(customer.getEmail())
                .privileges(customer.isPrivileges())
                .build();

        // Save entity; UUID will be generated by JPA
        CustomerEntity savedEntity = customRepository.save(customerEntity);

        // Update the Customer domain object with the generated ID
        customer.setCustomerId(savedEntity.getCustomerId());

    }
    @Override
    public Page<Customer> getPaginatedCustomers(Pageable pageable) {
        return customRepository.findAll(pageable).map(customerEntity -> new Customer(
                customerEntity.getCustomerId(),
                customerEntity.getName(),
                customerEntity.getEmail(),
                customerEntity.isPrivileges()
        ));
    }
    @Override
    public Optional<Customer> getCustomer(UUID id) {
        return customRepository.findById(id)
                .map(customerEntity -> {
                    // Map CustomerEntity to Customer
                    Customer customer = new Customer(
                            customerEntity.getCustomerId(),
                            customerEntity.getName(),
                            customerEntity.getEmail(),
                            customerEntity.isPrivileges()
                    );

                    // Map transactions and add them to the customer
                    customerEntity.getTransactions().forEach(transactionEntity -> {
                        Transaction transaction = new Transaction(
                                transactionEntity.getTransactionId(),
                                transactionEntity.getBorrowDate(),
                                transactionEntity.getReturnDate(),
                                transactionEntity.getDueDate(),
                                null, // Do not include the full customer to avoid recursion
                                null  // Map book if needed, else leave as null
                        );
                        transaction.setCustomerId(customerEntity.getCustomerId()); // Explicitly set customerId
                        transaction.setBookId(transactionEntity.getBook() != null ? transactionEntity.getBook().getBookId() : null); // Set bookId if book exists

                        customer.getTransactions().add(transaction); // Add to customer
                    });

                    return customer;
                });
    }
    @Override
    public Optional<Customer> getCustomerByName(String name) {
        return customRepository.findByName(name)
                .map(customerEntity -> new Customer(
                        customerEntity.getCustomerId(),
                        customerEntity.getName(),
                        customerEntity.getEmail(),
                        customerEntity.isPrivileges()
                ));
    }

    @Override
    public void updatePrivileges(Customer customer) {
        customRepository.findById(customer.getCustomerId())
                .ifPresentOrElse(customerEntity -> {
                    customerEntity.setPrivileges(customer.isPrivileges());
                    customRepository.save(customerEntity);
                }, () -> {
                    throw new EntityNotFoundException("Customer with ID " + customer.getCustomerId() + " not found");
                });
    }
    @Override
    public void updateCustomer(Customer customer) {
        customRepository.findById(customer.getCustomerId())
                .ifPresentOrElse(customerEntity -> {
                    customerEntity.setName(customer.getName());
                    customerEntity.setEmail(customer.getEmail());
                    customerEntity.setPrivileges(customer.isPrivileges());
                    customRepository.save(customerEntity);
                }, () -> {
                    throw new EntityNotFoundException("Customer with ID " + customer.getCustomerId() + " not found");
                });
    }

    @Override
    public void deleteCustomer(UUID id) {
        if (customRepository.existsById(id)) {
            customRepository.deleteById(id);
        } else {
            throw new RuntimeException("Customer with ID " + id + " not found!");
        }
    }
}
